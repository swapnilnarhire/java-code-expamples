/* 1. aIn the main method, declare variables of the following data types and initialize them
with sample values: */
public class DataTypesExample {
    public static void main(String[] args) {
        // Declare and initialize variables
        int sampleInt = 42;
        double sampleDouble = 3.14;
        char sampleChar = 'A';
        String sampleString = "Hello, Java!";
        boolean sampleBoolean = true;

        // Print the values
        System.out.println("Integer value: " + sampleInt);
        System.out.println("Double value: " + sampleDouble);
        System.out.println("Character value: " + sampleChar);
        System.out.println("String value: " + sampleString);
        System.out.println("Boolean value: " + sampleBoolean);
    }
}
